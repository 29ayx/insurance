    public void createUser(){
    
        
        String name = uNameBox.getText();
        String password = uPassBox.getText();
          
        int strtNo = Integer.parseInt(uStreetN.getText());
        String strt = uStreet.getText();
        String sub = uSub.getText();
        String city = uCity.getText();
        
        Address addr = new Address(strtNo, strt, sub, city);
        
        
        
        boolean result = company.addUser(new User(name, addr, password));
        
        if(result){
        JOptionPane.showMessageDialog(this, "User Added");
        clearFormCRT();
        fillUserTable();
        fillUserID();
        }else{
        
        JOptionPane.showMessageDialog(this, "Some error Occurred");
        }
        
    
    }
    
    public void fillUserTable() {

        ArrayList<User> users = company.users;

        String[] header = {"ID", "Name", "Password", "City", "Total Policies"};
        DefaultTableModel model = new DefaultTableModel(header, 0);
        usersTable.setModel(model);

        for (User x : users) {
            String[] row = new String[5];
            row[0] = String.valueOf(x.getUserID());
            row[1] = x.getName();
            row[2] = x.getUserPassword();
            row[3] = x.getAddress().getCity();
            row[4] = String.valueOf(x.totalPolicy());
            model.addRow(row);
        }
    }

    public void fillUserID() {

        userIDList.removeAllItems();

        for (User x : company.users) {
            userIDList.addItem(String.valueOf(x.getUserID()));
        }

    }

    public void fillPolicyList() {

        String z = String.valueOf(userIDList.getSelectedItem());
        int userID = Integer.parseInt(z);

        policyIDList.removeAllItems();
        ArrayList<InsurancePolicy> policies = company.findUser(userID).getPolicies();

        for (InsurancePolicy x : policies) {
            policyIDList.addItem(String.valueOf(x.getPolicyID()));
        }

    }

