    public void userReport() {

        int opt = reportBTN.getSelectedIndex();

        switch (opt) {

            case 0: {

                ArrayList<String> cities = company.populateDistinctCityNames();
                ArrayList<Double> paymentCity = company.getTotalPaymentPerCity(cities);

                String[] header = {"City Name ", "Total Payement Per City"};
                DefaultTableModel model = new DefaultTableModel(header, 0);
                userReport.setModel(model);

                for (int i = 0; i < cities.size(); i++) {
                    String[] row = new String[3];
                    row[0] = cities.get(i);
                    row[1] = String.valueOf(paymentCity.get(i));
                    model.addRow(row);

                }

            }
            break;
            case 1: {
                ArrayList<String> cars = company.populateDistinctCarModels();
                ArrayList<Integer> carCount = company.getTotalCountPerCarModel(cars);
                ArrayList<Double> paymentCars = company.getTotalPaymentPerCarModel(cars);
                

                String[] header = {"Car Model", "Total Premium", "Average Premium"};
                DefaultTableModel model = new DefaultTableModel(header, 0);
                userReport.setModel(model);

                for (int i = 0; i < cars.size(); i++) {
                    String[] row = new String[3];
                    row[0] = cars.get(i);
                    row[1] = String.valueOf(paymentCars.get(i));
                    row[2] = String.valueOf( paymentCars.get(i)/carCount.get(i) );
                    model.addRow(row);

                }
            }
            break;
        }

    }


