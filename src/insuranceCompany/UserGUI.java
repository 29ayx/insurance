    private void deletePolicyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletePolicyBtnActionPerformed
        // TODO add your handling code here:

        try
        {
            int row = policiesTable.getSelectedRow();
            String policy = policiesTable.getValueAt(row, 0).toString() ;

            int policyID = Integer.parseInt(policy);
            //JOptionPane.showMessageDialog(this,policyID);

            boolean result = user.deletePolicy(policyID);
            if (result){
                fillTable();
            }else{
                JOptionPane.showMessageDialog(this, "This policy cannot be deleted.");
            }

        }
        catch(ArrayIndexOutOfBoundsException e)
        {
            JOptionPane.showMessageDialog(this, "Please select the corresponding policy and then press Delete button.");
        }

    }//GEN-LAST:event_deletePolicyBtnActionPerformed

    private void updatePolicyBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatePolicyBtnActionPerformed
        // TODO add your handling code here:

        try
        {
            int row = policiesTable.getSelectedRow();
            String policyText = policiesTable.getValueAt(row, 0).toString() ;

            int policyID = Integer.parseInt(policyText);

            InsurancePolicy policy = user.findPolicy(policyID);
            String policyType = policy.policyType();

            UpdateFormGUI x = new UpdateFormGUI(this);

            if (policy!=null){

                switch(policyType){

                    case "TPP" : {
                        x.fillFormTPP((ThirdPartyPolicy) policy);
                        x.show();
                        fillTable();
                    }
                    break;
                    case "CHP" : {
                        x.fillFormCHP((ComprehensivePolicy) policy);
                        x.show();
                        fillTable();
                    }
                    break;
                    default : {
                        JOptionPane.showMessageDialog(this, "Something went wrong : (.");
                    }

                }

            }else{
                JOptionPane.showMessageDialog(this, "This policy cannot be updated.");
            }

        }
        catch(ArrayIndexOutOfBoundsException e)
        {
            JOptionPane.showMessageDialog(this, "Please select the corresponding policy and then press Update button.");
        }

    }//GEN-LAST:event_updatePolicyBtnActionPerformed

