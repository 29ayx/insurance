     public void fillTable() 
    {
        //HashMap<Integer, InsurancePolicy> policiesList = user.policies;
        //ArrayList<InsurancePolicy> policy = new ArrayList<InsurancePolicy>(policiesList.values());
        ArrayList<InsurancePolicy> policy = user.getPolicies();
        //Collections.sort();
        
        String [] header={"ID","Policy Holder Name","Car","Expiring (DD/MM/YY)","Total Premium"};
        DefaultTableModel model=new DefaultTableModel(header,0);
        policiesTable.setModel(model);
        
        for(InsurancePolicy x : policy)
        {
            String[] row=new String[5];
            row[0] = x.policyID+"";
            row[1] = x.policyHolderName;
            row[2] = (x.car.getModel()+" Price : "+x.car.getPrice());
            row[3] = (x.getExpiryDate().getDay()+"/"+x.getExpiryDate().getMonth()+"/"+x.getExpiryDate().getYear());
            row[4] = x.calcPremium(1)+"";
            model.addRow(row);
        }   
    }
     
     
     
     
     
     public void createPolicy(){
     
         String policyHolderName = policyNameBox.getText();
         int id = Integer.parseInt(InsuranceCompany.genPolicyID());
         
         String carModel = carModelBox.getText();
         String carTypeS = String.valueOf(carTypeBox.getSelectedItem());
         CarType type = CarType.valueOf(carTypeS);
         int manufacturingYear = Integer.parseInt(carYearBox.getText());
         double price = Double.parseDouble(priceBox.getText());
         Car car = new Car(carModel, type, manufacturingYear, price);
      
         
         int numberOfClaims = Integer.parseInt(claimBox.getText());
                 
         int day = Integer.parseInt(dateBox.getText());
         int month = Integer.parseInt(monthBox.getText());
         int year = Integer.parseInt(yearBox.getText());
         
         MyDate expiryDate = new MyDate(day, month, year);
         
         
         if(tppBTN.isSelected()){
         //Creates Third Party Policy
         String comments = commentBox.getText();
         
          ThirdPartyPolicy policy = new ThirdPartyPolicy(policyHolderName, id, car, numberOfClaims, expiryDate, comments);
          boolean result = user.addPolicy(policy);
          
          if(result){
          JOptionPane.showMessageDialog(this, "Policy Added Successfully !");
          clearPolicyForm();
          fillTable();
          fillFilterTable();
          }else{
          JOptionPane.showMessageDialog(this, "Some Error Occurred !");
          }
          
          
         }
         if(chpBTN.isSelected()){
         //Creates Comprehensive Policy
         
          int level = Integer.parseInt(levelBox.getText());
          int driverAge = Integer.parseInt(driverAgeBox.getText());
         
         
          ComprehensivePolicy policy = new ComprehensivePolicy(policyHolderName, id, car, numberOfClaims, expiryDate, driverAge, level);
          boolean result = user.addPolicy(policy);
          
          if(result){
          JOptionPane.showMessageDialog(this, "Policy Added Successfully !");
          clearPolicyForm();
          fillTable();
          fillFilterTable();
          }else{
          JOptionPane.showMessageDialog(this, "Some Error Occurred !");
          }
         }
     
     }
    
